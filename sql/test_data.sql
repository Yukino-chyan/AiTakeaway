FROM `order` o
JOIN (
    SELECT d.id, d.merchant_id, d.name, d.price
    FROM dish d
    INNER JOIN (SELECT merchant_id, MIN(id) AS min_id FROM dish GROUP BY merchant_id) m
        ON d.id = m.min_id
) d ON d.merchant_id = o.merchant_id
WHERE o.id BETWEEN 50001 AND 50060;

